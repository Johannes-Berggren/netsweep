export function lookupVendor(mac: string): string {
  // Normalize MAC: uppercase, use colons, take first 3 octets
  const normalized = mac.toUpperCase().replace(/-/g, ':');
  const prefix = normalized.split(':').slice(0, 3).join(':');

  return OUI_DATABASE[prefix] || 'Unknown';
}
